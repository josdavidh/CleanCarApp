package com.pascualbravo.models;

public class Vehiculos {
    private String placa;
    
    private String nombre; 
    
    private String modelo; 
    
    private String marca; 
    
    private String color;
    
    private int cedulaClnt;

    /**
     * @return the placa
     */
    public String getPlaca() {
        return placa;
    }

    /**
     * @param placa the placa to set
     */
    public void setPlaca(String placa) {
        this.placa = placa;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the modelo
     */
    public String getModelo() {
        return modelo;
    }

    /**
     * @param modelo the modelo to set
     */
    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    /**
     * @return the marca
     */
    public String getMarca() {
        return marca;
    }

    /**
     * @param marca the marca to set
     */
    public void setMarca(String marca) {
        this.marca = marca;
    }

    /**
     * @return the color
     */
    public String getColor() {
        return color;
    }

    /**
     * @param color the color to set
     */
    public void setColor(String color) {
        this.color = color;
    }

    /**
     * @return the cedulaClnt
     */
    public int getCedulaClnt() {
        return cedulaClnt;
    }

    /**
     * @param cedulaClnt the cedulaClnt to set
     */
    public void setCedulaClnt(int cedulaClnt) {
        this.cedulaClnt = cedulaClnt;
    }
    
}
